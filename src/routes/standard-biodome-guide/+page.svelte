  <section id="floor-plan">
    <h1>Floor Plan</h1>

    <p>28m&sup2; in a circle. The dome shape naturally creates zones: the center is for standing and activity, the edges are for resting and storage. Work with the shape, not against it.</p>

    <h3 id="suggested-layout">Suggested Layout</h3>
    <ul>
      <li><strong>Center zone (0-1.5m from center, ~7m&sup2;):</strong> Living space. Table, chairs, central heating if used (rocket mass heater or wood stove). Full headroom.</li>
      <li><strong>South arc (kitchen, ~5m&sup2;):</strong> Kitchen counter along the south wall near a window. Passive solar gain warms this zone in winter.</li>
      <li><strong>Sleeping platform (north arc, ~6m&sup2;):</strong> Low platform (30-40cm high) built against the north wall where the ceiling slopes down. The low ceiling over the platform is comfortable for lying down.</li>
      <li><strong>Entry zone (at door + wind porch, ~5m&sup2;):</strong> The wind porch provides ~2m&sup2; of enclosed airlock space for coats, boots, and weather transition. Inside the dome, a further ~3m&sup2; serves as the entry landing.</li>
      <li><strong>Storage perimeter (~7m&sup2;):</strong> Shelving and hooks around the lower walls where headroom is 1-1.8m. This ring of storage defines the interior zones without needing partition walls.</li>
    </ul>

    <figure class="figure">
      <img src="/assets/fig-floor-plan.svg" alt="Circular floor plan showing zones: center living, north sleeping, south kitchen, east entry, and storage perimeter." loading="lazy">
      <figcaption>Fig. 12 — Floor plan with suggested zones: sleeping (north), kitchen (south), entry (east)</figcaption>
    </figure>

    <h3 id="design-principles">Design Principles</h3>
    <ul>
      <li><strong>Door orientation:</strong> Face east or southeast. Morning sun warms the entry. Away from prevailing winter rain (which comes from the west/southwest in Portugal).</li>
      <li><strong>South window:</strong> Largest window on the south side for winter solar gain.</li>
      <li><strong>North side:</strong> No windows needed. Use this wall for the sleeping platform.</li>
      <li><strong>Built-in furniture:</strong> Benches and platforms following the curve are more space-efficient than freestanding furniture.</li>
      <li><strong>Heating:</strong> A small wood stove with chimney exiting through the dome wall (not the apex vent) is the most practical. Size it for the space — 28m&sup2; needs a very small stove.</li>
    </ul>

    <CalloutBox variant="tip" title="Bathroom">
      <p>At 28m&sup2;, an internal bathroom is possible but tight. A separate outdoor composting toilet and simple outdoor shower (solar-heated bag) is a better use of space. If you need an enclosed bathroom, consider a small attached structure outside the dome with its own roof.</p>
    </CalloutBox>
