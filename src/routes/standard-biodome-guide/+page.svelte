  <!-- Exterior Finish -->
  <div class="section-group" id="exterior">
    <h2>Exterior Finish</h2>
    <p>Stone, lime render, and climbing plants — no visible membrane</p>
  </div>

  <section>
    <p>The EPDM membrane is waterproof and durable, but it is black, industrial-looking, and degrades under prolonged UV exposure. The exterior finish system covers the entire membrane with natural materials: a stone skirt at the base, lime-earth render above, and climbing plants over both. The result is a dome that looks like it grew from the landscape.</p>

    <figure class="figure">
      <img src="/assets/fig-exterior-finish.svg" alt="Front view of the dome showing three exterior finish zones: stone skirt at base, lime-earth render above, and climbing plants growing over both." loading="lazy">
      <figcaption>Fig. 9 — Exterior finish: stone skirt (base), lime-earth render (upper), climbing plants</figcaption>
    </figure>

    <h3 id="stone-skirt">Zone 1: Stone Skirt (0–80cm above gabion)</h3>

    <p>Dry-stack local stone directly against the EPDM surface, from the gabion top to a height of 60–80cm. The EPDM prevents any moisture reaching the straw-clay beneath, so no air gap is needed.</p>

    <ul>
      <li><strong>Stone:</strong> Local granite or schist, 10–25cm pieces, flat faces outward</li>
      <li><strong>Thickness:</strong> 15–25cm (one stone depth)</li>
      <li><strong>Height:</strong> 60–80cm above the gabion top (90–110cm above grade)</li>
      <li><strong>Method:</strong> Dry-stack with no mortar. Lean slightly into the dome — the dome's inward curve naturally provides this. Stagger joints between courses.</li>
      <li><strong>Top course:</strong> Flat capstones to shed water outward, away from the render zone above</li>
    </ul>

    <CalloutBox variant="tip" title="Why 80cm Maximum?">
      <p>Above ~80cm, the dome wall curves inward steeply enough that stone begins to bear outward rather than pressing into the dome. Higher than this requires mortar or mechanical ties — more work for diminishing returns. At 80cm, the stone skirt protects the most vulnerable zone (ground splash, ember attack, UV at eye level) without added complexity.</p>
    </CalloutBox>

    <h3 id="lime-render">Zone 2: Lime-Earth Render (80cm to apex)</h3>

    <p>Above the stone skirt, apply a lime-earth render directly over the EPDM. This covers the membrane from the top of the stone to the apex vent collar.</p>

    <ProcessStep number={1} title="Key the Surface">
      <p>Drape galvanized wire mesh (25mm chicken wire) over the EPDM from the apex vent collar downward. Tuck the top edge under the collar flashing. At the bottom, tuck the lower edge behind the stone skirt — the weight of the stacked stone pins it in place. No screws through the membrane are needed: the dome's inward curve holds the mesh against the surface by gravity, and the render locks it permanently once applied. Overlap adjacent mesh strips by 10cm and twist-tie the edges together with short lengths of galvanized wire.</p>
    </ProcessStep>

    <ProcessStep number={2} title="Scratch Coat">
      <p>1 part hydraulic lime : 1 part clay soil : 4 parts sharp sand. Apply 8–10mm thick, pressing firmly into the mesh. Score with a comb while wet. Allow to cure for 5–7 days, misting daily to prevent too-rapid drying.</p>
    </ProcessStep>

    <ProcessStep number={3} title="Finish Coat">
      <p>1 part hydraulic lime : 0.5 parts clay soil : 3 parts fine sand. Apply 5–8mm over the cured scratch coat. Trowel to a smooth but not polished finish — some texture helps climbing plants grip.</p>
    </ProcessStep>

    <CalloutBox variant="tip" title="Zero Penetrations">
      <p>This method keeps the EPDM membrane completely intact — no screws, no sealant, no potential leak points. The dome's inward curve, gravity, and the stone skirt do all the work of holding the mesh in place.</p>
    </CalloutBox>

    <h3 id="climbing-plants">Zone 3: Climbing Plants</h3>

    <p>Plant fire-resistant climbing species at the base of the stone skirt. As they grow, they cover both the stone and the render, creating a living green exterior. The plants are rooted in the ground — they add no structural load to the dome.</p>

    <ul>
      <li><strong>Grape vine (<em>Vitis vinifera</em>):</strong> Deep-rooted, fire-resistant, deciduous. Provides summer shade and winter solar gain. Produces fruit.</li>
      <li><strong>Fig (<em>Ficus carica</em>):</strong> Can be trained as a wall climber. Very fire-resistant. Deciduous.</li>
      <li><strong>Virginia creeper (<em>Parthenocissus quinquefolia</em>):</strong> Self-clinging, fast coverage. Spectacular autumn colour. Deciduous — reveals the stone and render in winter.</li>
    </ul>

    <p>Plant 6–8 plants evenly spaced around the dome base. Train shoots upward using small galvanized hooks set into the stone skirt joints. Within 2–3 growing seasons, the plants will cover most of the dome surface.</p>

    <CalloutBox variant="danger" title="Fire Season Protocol">
      <p>Before fire season begins (June in Portugal):</p>
      <ol>
        <li><strong>Remove all dead and dry growth</strong> — strip any brown, dried, or dormant material from the dome surface. This is the actual ignition risk.</li>
        <li><strong>Prune green growth back to the stone skirt line</strong> (~80cm above gabion). Green grape vine and fig have very high moisture content and resist ignition, but above the stone zone they sit against lime render that can crack under intense radiant heat. Keep living growth on the stone skirt only.</li>
        <li><strong>Clear the 5m defensible space</strong> — no dead wood, brush, or dry grass within 5m of the dome.</li>
      </ol>
      <p>The stone skirt and lime render are non-combustible and protect the dome even without plant cover. Living, green growth on the stone zone adds no meaningful fire risk — but anything dead or dry must go.</p>
    </CalloutBox>

    <h3>Benefits</h3>
    <ul>
      <li><strong>UV protection:</strong> Covering the EPDM extends its lifespan from ~20 years to 30+ years</li>
      <li><strong>Fire resistance:</strong> Stone and lime render are non-combustible. The lower dome — most exposed to ground fire and embers — has the thickest protection.</li>
      <li><strong>Thermal mass:</strong> The stone skirt adds thermal mass at the base, stabilizing interior temperatures</li>
      <li><strong>Appearance:</strong> The dome blends into the landscape — stone base, earth-toned walls, green canopy</li>
      <li><strong>No visible EPDM:</strong> The entire membrane is hidden behind natural materials</li>
    </ul>
  </section>

