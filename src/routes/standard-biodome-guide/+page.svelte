    <h3 id="key-specs">Key Specifications</h3>
    <ul>
      <li><strong>Shape:</strong> Approximately catenary (stays predominantly in compression — no outward thrust)</li>
      <li><strong>Floor area:</strong> 28m² (comparable to a Mongolian ger)</li>
      <li><strong>Standing area:</strong> ~20m² where headroom exceeds 1.8m</li>
      <li><strong>Roof:</strong> EPDM membrane over straw-clay insulation</li>
      <li><strong>Exterior:</strong> Stone skirt, lime-earth render, climbing plants — no visible membrane</li>
      <li><strong>Crew:</strong> 2-3 people for entire build</li>
      <li><strong>Trees removed:</strong> 50-100 eucalyptus saplings and young trees</li>
      <li><strong>Build time:</strong> ~4-5 weeks in summer (longer in winter)</li>
      <li><strong>Lifespan:</strong> 25-35 years with maintenance</li>
    </ul>

