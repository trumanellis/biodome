    <h1>Interior Finish</h1>

    <h3 id="lime-plaster">Lime Plaster Walls</h3>
    <p>The interior of the wattle dome receives two coats of lime plaster. This seals the structure, reflects light, and provides fire resistance.</p>

    <ol>
      <li><strong>Scratch coat:</strong> 1 part hydraulic lime : 3 parts sharp sand. Press firmly into the wattle gaps. Score the surface with a comb while wet.</li>
      <li><strong>Finish coat:</strong> 1 part lime putty : 2.5 parts fine sand. Apply 3-5mm thick over the cured scratch coat. Trowel smooth.</li>
      <li><strong>Limewash:</strong> Thin lime putty diluted with water. Apply 3-4 coats by brush. This brightens the interior considerably — important in a dome where natural light is limited.</li>
    </ol>

    <CalloutBox variant="warning" title="Lime Burns Skin">
      <p>Wear gloves and eye protection when working with lime. Wash splashes immediately with water and vinegar.</p>
    </CalloutBox>

    <h3 id="floor">Floor</h3>
    <p>Three practical options:</p>

    <ul>
      <li><strong>Earthen floor (simplest):</strong> Compacted earth base, lime-stabilized top layer (1 part lime : 4 parts earth), sealed with linseed oil. Cool in summer, can be cold in winter — use rugs.</li>
      <li><strong>Lime concrete:</strong> 1 part hydraulic lime : 2 parts sand : 3 parts gravel, 10cm thick over compacted gravel base. More durable than earthen, warmer underfoot.</li>
      <li><strong>Timber deck:</strong> Raised timber floor on eucalyptus sleepers. Creates an air gap for insulation and moisture management. Most comfortable but most expensive.</li>
    </ul>
