  <div class="section-group" id="shell">
    <h2>Shell: Insulation &amp; Weatherproofing</h2>
    <p>Three layers that keep you warm and dry</p>
  </div>

  <section>
    <p>The shell turns the wattle skeleton into a weatherproof home. It goes on the outside of the dome in three layers: straw-clay for insulation, burlap for protection, and EPDM for waterproofing.</p>

    <figure class="figure">
      <img src="/assets/fig-shell-layers.svg" alt="Cut-away wall section showing five layers: lime plaster, wattle, straw-clay (100mm), burlap, and EPDM (1.5mm)." loading="lazy">
      <figcaption>Fig. 7 — Wall section showing all five layers (inside to outside)</figcaption>
    </figure>

    <h3 id="straw-clay">Layer 1: Straw-Clay Insulation (100mm)</h3>

    <p>Light straw-clay is a mix of loose straw coated in clay slip. It provides insulation (approximately R-7 at 100mm thickness), thermal mass, and fire resistance. Applied to the exterior of the wattle.</p>

    <ProcessStep number={1} title="Mix Clay Slip">
      <p>Soak clay soil in water overnight. Stir to a thick cream consistency (your hand dipped in and pulled out should be evenly coated, with no bare spots).</p>
    </ProcessStep>

    <ProcessStep number={2} title="Coat Straw">
      <p>Toss loose straw into the clay slip. Turn and squeeze until every strand is coated. The straw should be damp and sticky, not dripping wet.</p>
    </ProcessStep>

    <ProcessStep number={3} title="Apply from Bottom Up">
      <p>Press handfuls of coated straw onto the outside of the wattle, working it into the gaps. Build up in 25mm layers. Let each layer firm up (1-2 days in Portuguese summer, longer in winter) before adding the next. Four layers reaches 100mm.</p>
    </ProcessStep>

    <ProcessStep number={4} title="Upper Dome Application">
      <p>Above the dome's midpoint, gravity works against you. Apply thinner layers (15-20mm) and let each one dry fully. If material slumps, pin strips of burlap to the wattle with galvanized staples to hold the straw-clay in place while it dries.</p>
    </ProcessStep>

    <ProcessStep number={5} title="Final Surface">
      <p>Smooth the outer surface with a flat trowel or wet hands. Fill any cracks. The surface does not need to be perfect — it just needs to be smooth enough that the burlap layer sits flat against it.</p>
    </ProcessStep>

    <CalloutBox variant="warning" title="Drying Time">
      <p>Allow 2-4 weeks for the straw-clay to dry completely before applying the burlap and EPDM. In a Portuguese summer, 2 weeks is usually sufficient. In autumn or winter, allow 4 weeks or more. Tap the surface — it should sound hollow when dry.</p>
    </CalloutBox>

    <h3 id="burlap">Layer 2: Burlap Protection</h3>

    <p>A single layer of burlap (hessian) fabric draped over the dried straw-clay. This prevents the rough clay surface from puncturing the EPDM membrane.</p>

    <ul>
      <li>Drape from apex downward in overlapping strips</li>
      <li>Overlap each strip by 15cm</li>
      <li>Pin to the straw-clay with galvanized staples at 30cm intervals</li>
      <li>Pull smooth — no wrinkles or bunches</li>
    </ul>

    <h3 id="epdm">Layer 3: EPDM Waterproofing</h3>

    <p>EPDM (ethylene propylene diene monomer) rubber membrane is the waterproof skin of the dome. Use 1.5mm pond-grade EPDM.</p>

    <h4>Application Method: Horizontal Bands</h4>
    <p>Apply EPDM in horizontal bands, starting from the bottom and working up. Each band overlaps the one below it so water sheds over the seams, never under them.</p>

    <ol>
      <li>Cut EPDM into bands roughly 60-80cm wide</li>
      <li>Start at the base of the dome, just above the foundation</li>
      <li>Wrap the first band around the full circumference, overlapping the starting end by 15cm</li>
      <li>Apply EPDM primer to all overlap zones (both horizontal and vertical seams)</li>
      <li>Press EPDM seam tape firmly onto primed overlaps. Roll with a wallpaper seam roller</li>
      <li>Next band overlaps the top edge of the previous band by 15cm</li>
      <li>Continue to the crown. The final piece is a circular cap over the apex vent collar</li>
    </ol>

    <figure class="figure">
      <img src="/assets/fig-epdm-banding.svg" alt="Side view of dome with EPDM membrane in horizontal bands, overlap direction, base flashing, and apex rain cap." loading="lazy">
      <figcaption>Fig. 8 — EPDM horizontal bands: apply bottom-up, upper band always overlaps lower</figcaption>
    </figure>

    <CalloutBox variant="danger" title="Test Before You Finish">
      <p>After all EPDM is applied and seams are sealed, spray the entire dome with a garden hose for 30 minutes. Go inside. Inspect every seam. Fix any leaks immediately — they will only get worse. This test is not optional.</p>
    </CalloutBox>

    <h3 id="base-flashing">Base Flashing</h3>
    <p>Where the EPDM meets the gabion foundation, lap the membrane 10cm over the outer face of the gabion and secure with a stainless steel termination bar screwed into a treated timber batten embedded in the gabion top. This prevents wind uplift and directs water away from the foundation.</p>

    <p>The geotextile fold-over on top of the gabion prevents surface water from entering the stone fill. Between the EPDM directing rainwater outward and the geotextile cap sealing the top, the stone fill stays clean and the perforated pipe handles only subsurface water as intended.</p>

    <p>The exposed EPDM membrane is fully covered by the <a href="#exterior">exterior finish system</a> — stone, lime render, and climbing plants — described in the next section.</p>

    <figure class="figure">
      <img src="/assets/fig-base-flashing-detail.svg" alt="Cross-section through the dome base showing French drain trench, gabion foundation, EPDM base flashing lapped over the gabion outer face, termination bar, and stone skirt." loading="lazy">
      <figcaption>Fig. 8b — Base flashing detail: EPDM laps over gabion, secured by termination bar, with French drain below</figcaption>
    </figure>
  </section>

