  <section id="foundation">
    <h1>Foundation: Gabion Ring</h1>

    <p>The foundation is a 6m diameter ring of gabion baskets filled with local stone. It does four things: keeps the pole bases off wet ground, anchors the dome against wind, provides a level starting surface, and drains subsurface water away from the structure. The gabion ring doubles as a French drain — a geotextile-lined trench with a perforated pipe at the bottom collects groundwater and carries it to daylight downhill.</p>

    <h3 id="found-specs">Specifications</h3>
    <ul>
      <li><strong>Outer diameter:</strong> 6m</li>
      <li><strong>Ring width:</strong> 40cm</li>
      <li><strong>Height:</strong> 30cm above grade, 35cm below (65cm total)</li>
      <li><strong>Fill:</strong> Local granite or schist, 10-20cm pieces</li>
      <li><strong>Top surface:</strong> Flat stones creating level bearing for pole bases</li>
    </ul>

    <h3 id="drainage">Drainage Specifications</h3>
    <ul>
      <li><strong>Geotextile:</strong> Non-woven, 150–200 g/m², full wrap including fold-over top</li>
      <li><strong>Drain pipe:</strong> 100mm perforated HDPE (corrugated preferred), on 50mm gravel bed</li>
      <li><strong>Slope:</strong> 1–2% continuous fall toward outlet (~10cm total drop around circumference)</li>
      <li><strong>Outlet:</strong> 100mm solid pipe, &ge;2m to daylight downhill</li>
      <li><strong>Gravel:</strong> 20mm washed, 50mm deep under and around pipe</li>
    </ul>

    <figure class="figure">
      <img src="/assets/fig-foundation-plan.svg" alt="Top-down plan view of the 6m gabion ring with pole sockets and door opening." loading="lazy">
      <figcaption>Fig. 2 — Foundation plan view: 17 sockets (1 skipped for door = 16 active), drain pipe route, and outlet</figcaption>
    </figure>

    <h3 id="found-sequence">Construction Sequence</h3>

    <ProcessStep number={1} title="Mark the Circle">
      <p>Drive a stake at center. Attach a 3m string and scribe the outer perimeter. Then scribe the inner edge at 2.6m.</p>
    </ProcessStep>

    <ProcessStep number={2} title="Identify the Outlet Point">
      <p>Walk the perimeter with a spirit level. Find the lowest grade point — this is where the drain will exit. Mark it clearly. The trench bottom will slope toward this point from both directions around the ring.</p>
    </ProcessStep>

    <ProcessStep number={3} title="Excavate Ring Trench">
      <p>Dig 35cm deep at the outlet point, ~25cm deep at the opposite point. 50cm wide. This gives a 1–2% continuous slope from the high side to the outlet. Compact the bottom — errors here propagate through the entire build.</p>
    </ProcessStep>

    <ProcessStep number={4} title="Line with Geotextile and Lay Pipe">
      <p>Lay 160cm-wide non-woven geotextile strips along the trench, overlapping strips by 30cm. Spread a 50mm gravel bed (20mm washed gravel) on top of the geotextile at the bottom. Lay 100mm perforated HDPE pipe on the gravel bed with perforations facing down. At the outlet point, connect to a 100mm solid pipe that exits the ring and runs &ge;2m to daylight downhill.</p>
    </ProcessStep>

    <ProcessStep number={5} title="Assemble Gabion Cages">
      <p>3mm galvanized wire mesh, 10x10cm openings. Form into cages 40cm wide, 65cm tall. Wire-tie every 15cm. Lower cages into the lined trench. The geotextile wraps up between the cage and the trench wall on both sides.</p>
    </ProcessStep>

    <ProcessStep number={6} title="Fill and Level">
      <p>Pack gravel around the pipe first — 50mm cover above the pipe. Then fill with stone, compacting every 10cm. Fold the geotextile over the top of the fill before placing the final flat bearing stones. Check level around the entire perimeter with a long straight edge and spirit level.</p>
    </ProcessStep>

    <ProcessStep number={7} title="Pole Sockets">
      <p>At 16 equally-spaced points around the ring (every ~1.18m), leave a 12cm-wide gap in the top stones. These are the sockets for the main dome poles. At the position directly opposite the door, add a second socket right next to the first (15cm apart, center to center) — the fanning pair needs one socket per butt end. That gives 17 sockets total. Mark them clearly. Skip one socket where the door will go — you will install a door frame here instead.</p>
    </ProcessStep>

    <ProcessStep number={8} title="Wind Porch Gabion Extension">
      <p>At the door opening, build a U-shaped gabion extension for the wind porch foundation. Two arms project 1.2m outward from the main ring at each edge of the door opening, connected by a cross piece at the outer end. Same specs as the main ring: 40cm wide, 65cm deep, geotextile-lined, stone-filled, flat bearing stones on top. The U-shaped extension creates a level footing for the four porch corner posts and defines the porch floor area.</p>
    </ProcessStep>

    <ProcessStep number={9} title="Test the Drain">
      <p>Pour a bucket of water into the gabion fill at the point farthest from the outlet. Verify that water flows out at the daylight end within minutes. If flow is slow or absent, check the pipe slope and connections before proceeding.</p>
    </ProcessStep>

    <figure class="figure">
      <img src="/assets/fig-gabion-section.svg" alt="Cross-section of gabion foundation showing stone fill, wire mesh, flat bearing stones, pole socket with gravel packing." loading="lazy">
      <figcaption>Fig. 3 — Gabion ring cross-section: geotextile liner, drain pipe on gravel bed, stone fill, and pole socket</figcaption>
    </figure>

    <CalloutBox variant="tip" title="Protect the Outlet">
      <p>Fit a mesh cap on the daylight end of the outlet pipe to block rodents and debris. Keep the area around the outlet clear of soil buildup and vegetation so water can discharge freely.</p>
    </CalloutBox>

    <CalloutBox variant="warning" title="Let It Settle">
      <p>Wait 3-5 days before loading the foundation. Stone settling under weight is normal and better to happen before the poles go up.</p>
    </CalloutBox>
  </section>

  <!-- Frame -->
  <div class="section-group" id="frame">
    <h2>Dome Frame</h2>
    <p>Sixteen full-span poles paired into eight arches</p>
  </div>

  <section>
    <p>The dome frame is the skeleton of the structure. Sixteen full-span eucalyptus poles — each running the entire diameter of the dome — are paired into eight arches. Seven pairs cross diametrically. The eighth pair — the door-framing pair — starts at the two sockets opposite the door (one butt end each), crosses at the crown, and fans out on the door side so its thin ends land at the two sockets flanking the entrance. Each pole is bent by hand during installation: no bending forms, no weeks of drying on jigs. The poles are scored, soaked if needed, and walked up into position. Once lashed together at the crown, all eight pairs support each other and the wattle weave fills the spaces between them.</p>

    <h3 id="pole-specs">Pole Specifications</h3>
    <ul>
      <li><strong>Quantity:</strong> 16 main poles (plus 2 spares)</li>
      <li><strong>Diameter:</strong> 8-10cm at butt end</li>
      <li><strong>Length:</strong> ~11.6m (two 40cm embedments + 10.8m visible arc)</li>
      <li><strong>Species:</strong> <em>E. globulus</em> (blue gum / eucalipto-comum) — this is the eucalyptus in Portugal. It covers over 800,000 hectares and is almost certainly what you will be clearing. Denser and stiffer than some eucalypts, but bends well when green and properly scored. In highland areas (Beira Alta, Trás-os-Montes), you may also find <em>E. nitens</em> (shining gum), which bends slightly more easily.</li>
      <li><strong>Condition:</strong> Must be green — harvest within 2 weeks of use. Must be flexible enough to bend by hand during raising.</li>
    </ul>

    <CalloutBox variant="danger" title="Green Poles Only">
      <p>Dry eucalyptus is brittle and will snap during installation. The poles must be green — harvest within 2 weeks of use. <em>E. globulus</em> is denser than many eucalypts (~550 kg/m³ basic density), so select the youngest, straightest trees with the thinnest bark. Fast-growing saplings at 5-8 years old (8-10cm diameter) bend far better than slow-grown or older wood. Score the outer face thoroughly — this is not optional with <em>E. globulus</em>. If poles have been cut more than 2 weeks ago, soak in a water trough for 48-72 hours before installation.</p>
    </CalloutBox>

    <h3 id="preparing-poles">Preparing the Poles</h3>

    <ProcessStep number={1} title="Select and Pair">
      <p>Sort all 16 poles by butt-end diameter. Pair them so that each arch has two poles of similar size. Within each pair, mark the poles so their butt ends will be installed on opposite sides of the dome — this balances the taper across the arch. Number each pair (1-8) with a marking crayon or wire tag. Pair 8 is the door-framing pair — each butt end goes into its own socket at the back of the dome (the two sockets directly opposite the door).</p>
    </ProcessStep>

    <ProcessStep number={2} title="Score the Outer Face">
      <p>Using a draw knife or machete, score shallow cuts (1-2mm deep) every 15cm along the outer face — the face that will be on the outside of the curve when the pole is bent. Concentrate scoring on the middle third of the pole, where the bend is tightest. This relieves tension in the outer fibers and prevents cracking during live bending.</p>
    </ProcessStep>

    <ProcessStep number={3} title="Soak If Needed">
      <p>If poles have been cut more than 3 days ago or feel stiff when flexed by hand, soak in a water trough for 48-72 hours before raising. Freshly cut poles (within 2-3 days) typically have enough moisture to bend without soaking.</p>
    </ProcessStep>

    <figure class="figure">
      <img src="/assets/fig-bending-form.svg" alt="Full-span pole showing score marks concentrated on the middle third of the outer face, with butt and thin ends labeled." loading="lazy">
      <figcaption>Fig. 4 — Pole scoring detail: shallow cuts every 15cm on outer face, concentrated in the middle third</figcaption>
    </figure>

    <CalloutBox variant="tip" title="Pair Butt Ends Opposite">
      <p>Each pole tapers from butt (8–10 cm) to thin end. In the seven standard pairs, the two butt ends go into sockets on opposite sides of the dome. At each foundation point the thick butt of one pole sits alongside the thin tip of the other — evening out the cross-section on both sides. At the crown, both poles pass through at mid-diameter, keeping the apex light. (The fanning pair is the exception: both butts sit side by side at the back, and both thin ends sit side by side at the door flanks.)</p>
    </CalloutBox>

    <h3 id="raising-arches">Raising the Arches</h3>

    <p>With all poles scored and paired, assemble the dome frame. This is a two-person job.</p>

    <figure class="figure">
      <img src="/assets/fig-arch-sequence.svg" alt="Four-panel diagram: first arch pair with center post, second pair perpendicular, all eight arches, and crown crossing detail." loading="lazy">
      <figcaption>Fig. 5 — Arch raising sequence: first pair, second at 90°, all eight, and crown detail</figcaption>
    </figure>

    <ProcessStep number={1} title="First Pair">
      <p>Erect a temporary center post — a straight pole, 4m tall, braced upright at the dome center. Take the first pole of Pair 1. Set its butt end into a foundation socket, then two people walk the pole up and over the center post, bending it live, until the thin end reaches the opposing socket across the diameter. Wire-lash the pole to the center post at the apex. Now install the second pole of Pair 1 from the opposite side — its butt end goes into the socket next to where the first pole's thin end landed, and its thin end reaches the socket next to the first pole's butt. Lash both poles together at the crown.</p>
    </ProcessStep>

    <ProcessStep number={2} title="Second Pair (Perpendicular)">
      <p>Install the second pair at 90 degrees to the first. Walk each pole up and over center the same way. Where the four poles cross at the apex, lash them together with galvanized wire. The two pairs now stabilize each other. You can remove the temporary center post after this second pair is secure.</p>
    </ProcessStep>

    <ProcessStep number={3} title="Remaining Standard Pairs">
      <p>Install five more standard pairs (10 poles) at equal spacing (every 22.5 degrees), skipping the door position. Each new pair is walked up, bent live, and lashed to the existing poles where they cross at the crown. The structure gets more rigid with each pair added.</p>
    </ProcessStep>

    <ProcessStep number={4} title="Door-Framing Pair">
      <p>Install the eighth pair — the door-framing pair. Each butt end goes into its own socket — the two sockets side by side directly opposite the door. Walk the first pole up and over the crown, but instead of landing in the skipped door socket, angle its thin end to the flanking socket on one side of the door opening. Walk the second pole from the adjacent back socket, over the crown, and angle its thin end to the flanking socket on the other side. The two poles fan apart on the door side, naturally forming an arch over the entrance. Lash both to the crown crossing with the other 14 poles.</p>
    </ProcessStep>

    <ProcessStep number={5} title="Wind Porch Frame">
      <p>Install the wind porch frame immediately after the door-framing pair. Set four treated chestnut or pine corner posts (10cm diameter) plumb vertical into the U-shaped gabion extension: two inner posts at the dome face on each side of the door opening, two outer posts at the far end of the gabion arms. Posts rise to 1.5m — the arch spring line. Connect each side's inner and outer posts with a horizontal eave pole at 1.5m. Bend an arch frame from the left post top, over the peak, to the right post top — one arch at the inner end (peak at 2.2m, matching the dome door arch) and one at the outer end (peak at 2.0m, slightly lower for drainage). Install 5 arch rafters curving from the inner arch to the outer arch, forming a barrel-vault roof. The result is an arched tunnel projecting 1.2m outward from the dome, with a profile matching the door.</p>
    </ProcessStep>

    <ProcessStep number={6} title="Secure the Crown">
      <p>Once all 8 pairs are up, wrap the entire crown crossing with galvanized wire in a tight spiral. This zone — roughly 30cm across, where all 16 poles (8 pairs) overlap — becomes the structural keystone of the dome.</p>
    </ProcessStep>

    <ProcessStep number={7} title="Lock Pole Bases">
      <p>Pack each foundation socket tightly with gravel around the pole base. Then wire-lash each pole to the gabion mesh below it for uplift resistance in high winds.</p>

      <figure class="figure">
        <img src="/assets/render-shared-ch3-pole-gabion-connection.svg" alt="Vertical cross-section showing pole embedded in gabion with four protection layers: borax-treated core, 8-10mm char layer, HDPE wrap, and gravel packing within gabion stone" loading="lazy">
        <figcaption>Four protection layers at pole-gabion connection: borax-treated core, char layer, HDPE wrap, and gravel packing</figcaption>
      </figure>
    </ProcessStep>

    <CalloutBox variant="warning" title="Check the Shape">
      <p>Before proceeding, stand back and look at the dome from all sides. It should be symmetrical. Measure the height at center — it should be close to 4m. Adjust pole positions in their sockets if needed. Once you start weaving, adjustments become difficult.</p>
    </CalloutBox>
  </section>

