    <p>The 30-40cm opening at the crown of the dome serves as the primary ventilation. Hot air rises and exits here, drawing fresh air in through the door and windows.</p>

    <ul>
      <li><strong>Opening:</strong> 30-40cm diameter, formed at the center of the crown crossing where all 16 poles (8 pairs) overlap</li>
      <li><strong>Rain cap:</strong> A conical cap on a short post (like a chimney cowl), mounted 15-20cm above the opening. Allows air flow while deflecting rain.</li>
      <li><strong>Construction:</strong> Bend 4 thin poles into a cone, cover with a small piece of EPDM. Mount on a 20cm post anchored to the crown crossing.</li>
      <li><strong>Closure:</strong> For cold nights, stuff the opening with a removable cushion or plug from inside.</li>
    </ul>

