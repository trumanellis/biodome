  <section id="weave">
    <h1>Wattle Weave</h1>

    <p>Wattle is the oldest building technique in the world. Thin flexible poles woven between structural members to create a dense lattice. On this dome, horizontal rings and vertical infill turn the arch skeleton into a solid surface ready for straw-clay.</p>

    <CalloutBox variant="danger" title="Weave Green — Same Day as Harvest">
      <p>All wattle poles — rings and infill — must be woven while still green and pliable. Harvest them the same day you weave, or at most the day before. Eucalyptus dries fast: a 4cm pole left in Portuguese summer sun will stiffen within 24-48 hours and snap rather than bend.</p>
      <p>Wattle poles do <strong>not</strong> go through the 6-step treatment protocol. That process (charring, borax soak, arch bending, and weeks of drying) would leave them too stiff to weave. Instead, they are protected by encasement — straw-clay on the outside, lime plaster on the inside — which seals them from moisture and insects once the shell is complete.</p>
    </CalloutBox>

    <h3 id="horizontal-rings">Horizontal Rings</h3>

    <p>Install 7 horizontal rings at roughly 50cm vertical spacing up the dome. Each ring is a continuous circle of wattle poles woven in and out of the 16 main arch poles.</p>

    <ul>
      <li><strong>Poles:</strong> 5-7cm diameter, green eucalyptus</li>
      <li><strong>Ring 1 (lowest):</strong> ~50cm above foundation, circumference ~18m</li>
      <li><strong>Ring 7 (highest):</strong> ~3.5m up, circumference ~5m</li>
      <li><strong>Pattern:</strong> Weave in front of one arch pole, behind the next, alternating</li>
      <li><strong>Joining:</strong> Where pole ends meet, overlap by 30cm and wire-lash</li>
      <li><strong>Alternation:</strong> Each ring reverses the in/out pattern of the one below it</li>
    </ul>

    <p>The lower rings are long — you may need 2-3 poles spliced end-to-end to complete one ring. Upper rings may need only one pole bent into a circle.</p>

    <figure class="figure">
      <img src="/assets/fig-wattle-weave.svg" alt="Detail of wattle weave pattern showing arch poles, horizontal rings weaving in/out, and vertical infill poles." loading="lazy">
      <figcaption>Fig. 6 — Wattle weave pattern: horizontal rings alternate in/out, vertical infill fills gaps</figcaption>
    </figure>

    <h3 id="vertical-infill">Vertical Infill</h3>

    <p>Between each pair of horizontal rings, weave vertical wattle poles to fill the gaps.</p>

    <ul>
      <li><strong>Poles:</strong> 3-5cm diameter, 1-1.5m length, green — harvest the same day you weave</li>
      <li><strong>Spacing:</strong> Every 10-15cm around the dome</li>
      <li><strong>Pattern:</strong> Weave behind one horizontal ring, in front of the next</li>
      <li><strong>Quantity:</strong> Approximately 150-200 poles total</li>
    </ul>

    <p>Work from the bottom up. The lower sections have wider spacing between arch poles (more infill needed). The upper sections converge and need less infill.</p>

    <CalloutBox variant="tip" title="Leave the Door Opening Clear">
      <p>The arch door spans two pole bays. Do not weave across this double-wide opening — weave up to the door frame posts on each side and stop. Above the arch peak (2.2m), the structure closes normally.</p>
    </CalloutBox>

    <h3 id="density-check">Density Check</h3>
    <p>When the weave is complete, you should not be able to push your fist through any gap in the lattice. If you can, add more infill poles. The straw-clay needs a tight matrix to key into.</p>
  </section>

