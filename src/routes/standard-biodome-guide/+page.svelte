  <section id="overview">
    <h1>Design Overview</h1>

    <p>The Standard BioDome is a 6-meter wattle dome. Sixteen full-length eucalyptus poles, paired into eight arches, span the full dome diameter and cross at the apex. Seven pairs cross straight across; the eighth pair fans from two sockets opposite the door to frame the entrance. Wattle weave fills the gaps. Straw-clay insulates the outside. EPDM keeps the rain out. Two or three people can build the whole thing.</p>

    <p>There is no ring beam, no complex rafter assembly. The dome is one continuous structure from foundation to apex. The shape approximates a catenary — the natural curve a hanging chain makes, inverted. The poles are not forced into a mathematically precise shape; they find their own curve under gravity. This keeps the structure predominantly in compression, which is what timber does best.</p>

