  <div class="section-group" id="phases">
    <h2>Construction Phases</h2>
    <p>Foundation to finished home</p>
  </div>

  <section>
    <p>This timeline assumes 2-3 people working most days. Drying periods are not idle time — use them to prepare materials for the next phase.</p>

    <ProcessStep number={1} title="Site Prep & Pole Harvest (Week 1)">
      <ul>
        <li>Clear eucalyptus from building footprint + 3m buffer (including porch area)</li>
        <li>Mark center point and 3m radius</li>
        <li>Harvest 18 arch poles (8-10cm, ~11.6m) — green (16 needed + 2 spares)</li>
        <li>Harvest ring poles for horizontal rings (5-7cm dia) — these can be borax-soaked with the arch poles if desired, or kept green under shade wraps for use in Weeks 3-4</li>
        <li>Score and pair arch poles (8 pairs, including door-framing pair)</li>
        <li>Treat door, window frame, and wind porch corner posts (borax soak)</li>
      </ul>
    </ProcessStep>

    <ProcessStep number={2} title="Foundation (Weeks 1-2)">
      <ul>
        <li>Identify outlet point (lowest grade)</li>
        <li>Excavate ring trench (35cm deep at outlet, sloped 1–2%)</li>
        <li>Line with geotextile, lay gravel bed and perforated pipe</li>
        <li>Connect outlet pipe to daylight downhill</li>
        <li>Assemble gabion cages, fill with stone over pipe</li>
        <li>Fold geotextile over top, level bearing surface, mark sockets</li>
        <li>Build wind porch U-shaped gabion extension at door opening</li>
        <li>Test drain, allow foundation to settle (3–5 days)</li>
      </ul>
    </ProcessStep>

    <ProcessStep number={3} title="Dome Frame (Week 2-3)">
      <ul>
        <li>Erect temporary center post (4m)</li>
        <li>Raise all 8 arch pairs (7 standard + 1 door-framing) — bend poles live</li>
        <li>Install wind porch frame: 4 corner posts, ridge poles, rafters, outer lintel</li>
        <li>Wire-lash crown crossing (all 16 poles)</li>
        <li>Lock pole bases into foundation</li>
        <li>Install door frame at porch outer end</li>
      </ul>
    </ProcessStep>

    <ProcessStep number={4} title="Wattle Weave (Weeks 3-4)">
      <ul>
        <li>Harvest wattle infill poles fresh (3-5cm dia, ~200 pcs) — weave the same day</li>
        <li>Install 7 horizontal rings from bottom up</li>
        <li>Weave vertical infill between rings</li>
        <li>Weave wind porch sidewalls and roof infill</li>
        <li>Install window frames and window box frame members</li>
        <li>Density check — no gaps larger than a fist</li>
      </ul>
    </ProcessStep>

    <ProcessStep number={5} title="Straw-Clay Shell (Weeks 4-5 + 2-4 weeks drying)">
      <ul>
        <li>Mix clay slip, coat straw</li>
        <li>Apply to exterior in 25mm layers, bottom up</li>
        <li>Apply straw-clay to wind porch sidewalls (same 100mm build-up)</li>
        <li>Fill window box cheek walls with small-scale wattle and straw-clay</li>
        <li>Pin burlap strips on upper dome to hold material</li>
        <li>Smooth final surface</li>
        <li>Allow 2-4 weeks to dry completely</li>
      </ul>
      <p><em>Use drying time to prepare interior: compact floor base, mix lime plaster, build furniture.</em></p>
    </ProcessStep>

    <ProcessStep number={6} title="Weatherproofing (Weeks 5-6)">
      <ul>
        <li>Drape burlap over dried straw-clay, staple in place</li>
        <li>Apply porch EPDM first (roof, sidewalls) at the dome junction zone</li>
        <li>Apply dome EPDM in horizontal bands, bottom up — lap OVER porch EPDM at junction</li>
        <li>Cut EPDM picture-frame at each window, fold flaps into box, seal</li>
        <li>Install window sill flashing with 5-degree outward slope and drip edge</li>
        <li>Seal all seams with primer + tape (15cm overlap minimum)</li>
        <li>Install base flashing at gabion</li>
        <li>Build and install apex rain cap</li>
        <li><strong>Hose test for 30 minutes — fix all leaks, especially porch junction and window sills</strong></li>
      </ul>
    </ProcessStep>

    <ProcessStep number={7} title="Exterior Finish (Weeks 6-7)">
      <ul>
        <li>Stack stone skirt against EPDM from gabion top to 60-80cm — extend across porch sidewalls</li>
        <li>Drape wire mesh over EPDM above stone zone, tuck under collar and behind stone skirt</li>
        <li>Apply lime-earth render scratch coat (8-10mm) to dome and porch upper walls, allow to cure 5-7 days</li>
        <li>Apply lime-earth render finish coat (5-8mm)</li>
        <li>Plant climbing vines at base of stone skirt</li>
      </ul>
      <p><em>The stone skirt can begin as soon as the EPDM hose test passes. Render and interior plaster can proceed in parallel.</em></p>
    </ProcessStep>

    <ProcessStep number={8} title="Interior Finish (Weeks 7-9)">
      <ul>
        <li>Apply lime plaster scratch coat to interior (including porch interior)</li>
        <li>Install floor (earthen, lime concrete, or timber)</li>
        <li>Apply lime plaster finish coat</li>
        <li>Apply 3-4 coats of limewash</li>
        <li>Install door at porch outer frame, glaze windows in outer box frames</li>
      </ul>
    </ProcessStep>

    <CalloutBox variant="tip" title="Real Timeline">
      <p>The active work takes about 20-25 days spread over 4-9 weeks, depending on drying conditions. In a Portuguese summer, drying is fast and you can finish in 4-5 weeks. In winter, allow 8-10 weeks for straw-clay, exterior render, and interior plaster to cure properly.</p>
    </CalloutBox>
