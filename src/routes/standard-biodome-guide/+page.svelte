  <section id="openings">
    <h1>Door, Wind Porch, Windows &amp; Apex Vent</h1>

    <h3 id="arch-door">Arch Door (Double Bay)</h3>

    <p>The door spans a double bay — two pole spacings wide, created by skipping one socket. The eighth arch pair — the door-framing pair — fans from the two sockets opposite the door, and its thin ends land at the two sockets flanking the opening, forming a natural arch over the entrance. This gives a generous ~2m-wide doorway.</p>

    <ul>
      <li><strong>Width:</strong> ~2m (spanning two pole bays)</li>
      <li><strong>Shape:</strong> The fanning pair curves apart as it descends from the crown, creating a pointed arch that separates at roughly 2.2m height. Below that, two treated chestnut or pine posts (10cm diameter) stiffen the straight sides down to the foundation.</li>
      <li><strong>Frame:</strong> Two treated chestnut or pine posts set into the gabion, rising to 1.5m. Above 1.5m, the fanning pair's descending thin ends provide the arch.</li>
      <li><strong>Threshold:</strong> Treated timber sill beam across the foundation at the base of the opening</li>
      <li><strong>Door itself:</strong> Double ledge-and-brace timber doors (each leaf ~1m wide), or a heavy curtain for warm months</li>
    </ul>

    <p>Install the door frame posts during the dome frame phase, before weaving. The door-framing pair is installed after the seven standard pairs (see <a href="#frame">Raising the Arches, Step 4</a>). The wattle weave terminates at the door edges on each side.</p>

    <h3 id="wind-porch">Wind Porch (Katak)</h3>

    <p>The 6m catenary dome wall leans roughly 15 degrees inward from vertical at the base. A door frame mounted directly on this slanted surface will not hang plumb, the sill will slope inward, and rain drives straight in. The wind porch solves all three problems: a short arched tunnel projecting outward from the dome at the door, inspired by the igloo katak (entrance tunnel). The porch arch profile matches the door — straight walls up to 1.5m, then a curved arch to the peak — so the two read as one continuous opening.</p>

    <figure class="figure">
      <img src="/assets/fig-wind-porch-plan.svg" alt="Top-down plan view showing U-shaped gabion extension, corner posts, and porch floor area projecting from the dome at the door opening." loading="lazy">
      <figcaption>Fig. 10a — Wind porch plan view: U-shaped gabion arms, corner posts, and clear floor area</figcaption>
    </figure>

    <h4>Dimensions</h4>
    <ul>
      <li><strong>Projection:</strong> 1.2m outward from dome face</li>
      <li><strong>Width:</strong> 2m (matches double-bay door opening)</li>
      <li><strong>Height:</strong> 2.2m at inner end (dome arch), 2.0m at outer door frame</li>
      <li><strong>Internal clear depth:</strong> ~1.0m (after wall thickness)</li>
    </ul>

    <h4>Structure</h4>
    <ul>
      <li>4 corner posts (10cm treated chestnut/pine, plumb vertical) in gabion footings — posts rise to 1.5m (where the arch begins)</li>
      <li>2 side eave poles connecting inner to outer posts at 1.5m (arch spring line)</li>
      <li>Inner arch frame matching the dome door arch profile (peak at 2.2m)</li>
      <li>Outer arch frame matching the same profile (peak at 2.0m — slightly lower for roof drainage)</li>
      <li>5 arch rafters curving from inner arch to outer arch, forming a barrel-vault roof</li>
      <li>Wattle-weave sidewalls below the arch spring line and infill between rafters</li>
      <li>Same layer stack as dome: straw-clay (100mm), burlap, EPDM, stone skirt + lime render</li>
    </ul>

    <h4>Foundation</h4>
    <p>U-shaped gabion extension: two 1.2m arms projecting from the main ring at the door edges, connected by a cross piece. Same specs as the main ring (40cm wide, 65cm deep, geotextile-lined). See <a href="#foundation">Foundation, Step 8</a>.</p>

    <figure class="figure">
      <img src="/assets/fig-wind-porch-section.svg" alt="Side cross-section through door center showing porch structure, layer stack, EPDM junction detail, and roof slope." loading="lazy">
      <figcaption>Fig. 10b — Wind porch cross-section: layer stack, EPDM junction, and roof drainage slope</figcaption>
    </figure>

    <CalloutBox variant="danger" title="EPDM Junction — Critical Detail">
      <p>The junction between the dome EPDM and the porch EPDM is the single most leak-prone point on the structure. Follow this order exactly:</p>
      <ol>
        <li><strong>Apply porch EPDM first</strong> at the junction zone — roof and sidewalls of the porch.</li>
        <li><strong>Apply dome EPDM lapping OVER the porch EPDM</strong> — water flows dome, then porch, then outward. The upper layer always overlaps the lower.</li>
        <li><strong>All junctions:</strong> 15cm overlap, EPDM primer on both surfaces, seam tape rolled firmly.</li>
      </ol>
      <p>Include this junction in every hose test and every seasonal inspection.</p>
    </CalloutBox>

    <CalloutBox variant="tip" title="What the Porch Gives You">
      <p>A plumb outer door frame (doors hang straight, sills are level). A thermal airlock (interior stays warm when you open the outer door). A rain and wind buffer (no direct weather exposure). A place to hang coats and remove boots. All built from the same materials as the dome.</p>
    </CalloutBox>

    <h3 id="arch-windows">Arch Windows &amp; Window Boxes</h3>

    <p>Install 2-3 arch-topped windows in the natural bays between adjacent arch poles. The poles act as the window jambs, so you only need to add a sill beam, two short vertical stiles, and a curved arch frame at the top.</p>

    <p>Like the door, each window sits on a dome wall that leans inward. A 20cm-deep window box projects outward from each opening, giving a plumb outer frame for the glazing.</p>

    <ul>
      <li><strong>Shape:</strong> Rectangular below, arch-topped above — following the natural curve between two poles</li>
      <li><strong>Size:</strong> ~95cm wide (the natural pole spacing) &times; ~110cm from sill to arch peak</li>
      <li><strong>Sill height:</strong> 70cm above foundation (comfortable seated eye level)</li>
      <li><strong>Inner frame:</strong> Treated chestnut or pine: horizontal sill beam, two short stiles up to where the arch begins (~1.3m), then a curved arch frame to the peak (~1.8m). Install during the dome frame phase, before weaving.</li>
      <li><strong>Window box:</strong> 4 short treated frame members (20cm long) at each corner project outward from the dome EPDM surface. Small-scale wattle and straw-clay cheek walls fill between the dome surface and the outer frame.</li>
      <li><strong>Outer frame:</strong> Plumb frame at the outer end of the box, with rebate for glazing.</li>
      <li><strong>Glazing:</strong> Fixed polycarbonate sheet (6mm), mounted in the plumb outer frame with butyl tape seal. The polycarbonate sits in the rebate of the outer frame — plumb, not leaning.</li>
      <li><strong>Sill slope:</strong> 5 degrees outward for drainage, with an EPDM drip edge extending 5cm past the outer face.</li>
      <li><strong>Placement:</strong> At least one window opposite the door for cross-ventilation. Orient the largest window south for passive solar gain in winter.</li>
    </ul>

    <h4>Window Box EPDM Detail</h4>
    <p>Cut the dome EPDM in a picture-frame pattern at each window opening. Fold the four flaps into the box sides and seal to the frame members with primer and seam tape. The sill flashing is a separate EPDM piece that covers the sill, slopes outward at 5 degrees, laps over the dome EPDM below the window, and extends 5cm past the outer face as a drip edge.</p>

    <figure class="figure">
      <img src="/assets/fig-window-box-section.svg" alt="Horizontal section through window showing projecting box frame, EPDM picture-frame flaps, sill flashing with drip edge, and polycarbonate glazing in plumb outer frame." loading="lazy">
      <figcaption>Fig. 10c — Window box horizontal section: EPDM flaps, sill flashing, glazing in plumb frame</figcaption>
    </figure>

    <CalloutBox variant="tip" title="Keep It Simple">
      <p>In a Portuguese climate, you can get away with fewer windows than you think. The apex vent and door provide substantial airflow. Three arch windows — one south, one on the side, and one opposite the door — give good light and cross-ventilation.</p>
    </CalloutBox>

    <figure class="figure">
