  <section id="maintenance">
    <h1>Maintenance</h1>

    <h3 id="first-year">First Year (Monthly)</h3>
    <ul>
      <li>Check eucalyptus stumps for regrowth — treat immediately</li>
      <li>Inspect EPDM seams after heavy rain — especially the wind porch junction and window sill flashing</li>
      <li>Check lime plaster for cracks as poles continue to dry and shrink (fill with lime putty)</li>
    </ul>

    <h3 id="seasonal">Seasonal</h3>

    <h4>Spring</h4>
    <ul>
      <li>Inspect EPDM at base flashing, apex vent collar, wind porch junction, and window sill flashing — the covered portions are UV-protected</li>
      <li>Touch up interior lime plaster cracks and exterior lime-earth render cracks (including porch walls)</li>
      <li>Re-stack any frost-shifted stones in the stone skirt (dome and porch)</li>
      <li>Train new climbing plant growth onto dome surface</li>
    </ul>

    <h4>Summer</h4>
    <ul>
      <li>Fire season vigilance — maintain 5m defensible space</li>
      <li>Strip all dead and dry plant growth from dome surface</li>
      <li>Prune green growth back to stone skirt line (~80cm) — living green growth on stone zone is acceptable</li>
      <li>Check apex vent rain cap, ensure airflow is clear</li>
    </ul>

    <h4>Autumn</h4>
    <ul>
      <li>Apply limewash touch-up to interior</li>
      <li>Test foundation drain — pour water into gabion, verify flow at outlet within minutes</li>
      <li>Clear outlet pipe daylight end — remove leaves, soil, vegetation</li>
      <li>Check outlet mesh cap — replace if corroded</li>
      <li>Check stone skirt drainage — clear any soil buildup trapping water against stone</li>
      <li>Prepare for wet season: verify EPDM seams at base flashing and apex are sound</li>
    </ul>

    <h4>Winter</h4>
    <ul>
      <li>Monitor for leaks during heavy rain — fix immediately</li>
      <li>Check base flashing where EPDM meets gabion</li>
      <li>Ensure apex vent plug is available for cold nights</li>
    </ul>

    <h3 id="long-term">Long-Term</h3>
    <ul>
      <li><strong>Every 3-5 years:</strong> Reapply limewash to interior (2-3 coats). Patch exterior lime-earth render where cracked or thinned.</li>
      <li><strong>Every 5-10 years:</strong> Inspect EPDM at base flashing and apex vent collar (the only exposed sections). The covered portions are UV-protected and should outlast exposed EPDM significantly.</li>
      <li><strong>Every 10-15 years:</strong> Inspect wire lashings at crown and base. Replace if corroded. Re-render exterior if erosion has thinned it below 10mm. Flush drain pipe with a garden hose from the outlet end; rod if flow is significantly reduced.</li>
      <li><strong>At 25-30 years:</strong> EPDM replacement may be needed. The exterior finish extends membrane life well beyond the typical 20-year exposed lifespan. Remove render and stone, replace membrane, re-apply finish.</li>
      <li><strong>At 25-35 years:</strong> Assess pole condition. Well-maintained straw-clay keeps poles dry and functional for decades.</li>
    </ul>

