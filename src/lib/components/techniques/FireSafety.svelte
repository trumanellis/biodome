    <h1>Fire Safety &amp; Native Restoration</h1>

    <h2>Structure Fire Resistance</h2>

    <p>All BioDome designs have <strong>exceptional fire resistance</strong>:</p>

    <ul>
        <li><strong>Gabion stone foundation:</strong> Rock in wire mesh—completely non-combustible</li>
        <li><strong>Light straw-clay walls:</strong> Clay-coated straw chars but doesn't sustain flame</li>
        <li><strong>Living roof:</strong> Growing plants hold moisture. Soil absorbs heat without igniting</li>
    </ul>

    <h2>Optional: Stone Apron for Enhanced Protection</h2>

    <p>In high-risk fire zones, consider adding a <strong>stone apron</strong> to the lower portion of walls:</p>

    <ul>
        <li><strong>Height:</strong> 50-100cm from foundation top</li>
        <li><strong>Materials:</strong> Local granite, dry-stacked or lime-mortared</li>
        <li><strong>Thickness:</strong> 10-15cm facing, tied into wall with galvanized wire</li>
    </ul>

    <h2 id="native-restoration">Native Forest Restoration</h2>

    <p>The most effective fire prevention is <strong>replacing eucalyptus with native forest</strong>. Every eucalyptus you harvest is one less fire ladder. Every native tree you plant is long-term fire insurance.</p>

    <CalloutBox variant="tip" title="Replanting Priorities">
        <p>Focus on fire-resistant natives: <strong>Quercus robur</strong> (pedunculate oak), <strong>Quercus suber</strong> (cork oak), <strong>Castanea sativa</strong> (sweet chestnut), and <strong>Arbutus unedo</strong> (strawberry tree).</p>
    </CalloutBox>

