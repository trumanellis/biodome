<section id="finishing" data-component="Finishing">
    <h1>Finishing Techniques</h1>

    <h2 id="lime-render">Lime Render</h2>

    <ul>
        <li><strong>Base coat:</strong> 1:3 lime:sand, 15mm thick</li>
        <li><strong>Finish coat:</strong> 1:2.5 lime:fine sand, 5mm thick</li>
        <li><strong>Limewash:</strong> 3-4 coats for final protection</li>
    </ul>

    <CalloutBox variant="tip" title="Why Lime, Not Cement">
        <p>Lime render is breathable — it allows moisture to pass through the wall rather than trapping it. Cement render on straw-clay walls creates a vapor barrier that leads to rot. Lime also self-heals small cracks through carbonation.</p>
    </CalloutBox>

    <h2 id="openings">Windows &amp; Doors</h2>

    <CalloutBox variant="warning" title="Use Chestnut for Precision Joinery">
        <p>While eucalyptus is ideal for structural framing (posts, wattle poles, rafters, purlins), its tendency to warp and twist as it dries makes it unsuitable for <strong>precision millwork</strong>. Window frames, door frames, and mullions require dimensional stability for tight weatherseals and smooth operation. For these applications, use <strong>chestnut (<em>Castanea sativa</em>)</strong> — a native Mediterranean hardwood with excellent stability, natural rot resistance from high tannin content, and a long tradition of use in Portuguese building.</p>
    </CalloutBox>

    <p><strong>Deep reveals</strong> (40cm+) create natural window seats.</p>

    <h3 id="sill-pan">Sill Pan (All Windows)</h3>
    <ul>
        <li><strong>Slope:</strong> Window sills must slope outward at minimum 15&deg; (1:4 ratio)</li>
        <li><strong>Pan liner:</strong> Install EPDM or metal pan liner beneath window frame, extending 50mm up frame jambs and 100mm beyond sill edge</li>
        <li><strong>Drip edge:</strong> Sill must project 40mm beyond wall face with undercut drip groove</li>
        <li><strong>End dams:</strong> Turn up pan liner at both ends to prevent water running along sill into wall</li>
    </ul>

    <h3 id="head-flashing">Head Flashing (All Openings)</h3>
    <ul>
        <li><strong>Z-flashing:</strong> Install galvanized or aluminum Z-flashing above all window and door heads</li>
        <li><strong>Extension:</strong> Flashing must extend 50mm beyond frame on each side</li>
        <li><strong>Overlap:</strong> Lime render overlaps top leg of flashing; bottom leg overlaps frame head</li>
    </ul>

    <h3 id="door-threshold">Door Threshold</h3>
    <ul>
        <li><strong>Elevation:</strong> Door threshold must be minimum 50mm above exterior grade</li>
        <li><strong>Drainage:</strong> Slope exterior surface away from threshold at 2% minimum</li>
        <li><strong>Weather strip:</strong> Install adjustable door sweep or threshold seal</li>
    </ul>

    <CalloutBox variant="danger" title="Avoid Roundwood Frames for Exterior Openings">
        <p>Roundwood makes attractive interior trim but performs poorly at weathered exterior openings. The irregular surface creates gaps that are difficult to seal. Use kiln-dried, dimensioned timber (chestnut, oak, or treated softwood) for all window and door frames exposed to weather.</p>
    </CalloutBox>

    <h2 id="floor-options">Floor Options</h2>

    <ol>
        <li><strong>Earthen floor:</strong> Clay/sand/linseed finish (beautiful, requires maintenance)</li>
        <li><strong>Flagstone on sand:</strong> Durable, attractive, low maintenance</li>
        <li><strong>Polished concrete:</strong> Practical, can integrate radiant heating</li>
    </ol>
</section>

