    <h2 id="tools-checklist">Essential Tools Checklist</h2>

    <h3>Eucalyptus Eradication</h3>
    <ul>
        <li><strong>Herbicide applicator:</strong> Squeeze bottle with flip-top, triclopyr gel, or dauber bottle</li>
        <li>Nitrile gloves (multiple pairs)</li>
        <li>Safety glasses</li>
    </ul>

    <h3>Pole Harvesting &amp; Treatment</h3>
    <ul>
        <li>Chainsaw + safety gear (chaps, helmet, ear protection)</li>
        <li>Drawknife or bark spud (for debarking)</li>
        <li>Soaking trough: 6m × 0.8m × 0.5m trench lined with EPDM or heavy poly (see <a href="#treatment">Treatment section</a>)</li>
        <li>Weights to keep poles submerged (stones or weighted board)</li>
        <li>Propane torch with regulator and 5m hose</li>
        <li>Stiff wire brush (for cleaning char)</li>
        <li>Cooking thermometer (to verify linseed oil at 60-70°C)</li>
        <li>Pin moisture meter (to verify pole dryness ≤20%)</li>
    </ul>

    <h3>Construction</h3>
    <ul>
        <li>Long spirit level (2m minimum) or laser level</li>
        <li>String line and stakes (for layout)</li>
        <li>Drill with 6mm and 10mm bits</li>
        <li>Heavy hammer or mallet</li>
        <li>Wire cutters and pliers</li>
        <li>Chisel set (for cutting notches)</li>
        <li>Come-along or ratchet straps</li>
        <li>Scaffolding or sturdy ladders</li>
    </ul>

    <h3>Temporary Construction Items</h3>
    <ul>
        <li><strong>Center support tower:</strong> ~4.5m tall temporary structure for roof assembly</li>
        <li><strong>Ground rehearsal space:</strong> 12m × 12m clear flat area for laying out roof</li>
        <li><strong>Blocking and wedges:</strong> Wood scraps for shimming</li>
    </ul>

